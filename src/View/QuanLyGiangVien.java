    private void dataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTableMouseClicked
        int i = dataTable.getSelectedRow();
        GiangVien selectedRow = Main_Model.DanhSachGV.get(i);
        ima.setText(selectedRow.getMa());
        iten.setText(selectedRow.getTen());
        isdt.setText(selectedRow.getSdt());
        cbmonday.getModel().setSelectedItem(selectedRow.getMonDay());
        if(selectedRow.getGioiTinh().equals("Nam"))
            rnam.setSelected(true);
        else 
            rnu.setSelected(true);
    }//GEN-LAST:event_dataTableMouseClicked

