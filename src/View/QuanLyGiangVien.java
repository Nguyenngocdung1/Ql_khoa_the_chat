    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         // TODO add your handling code here:
        if(!isFill()) return;
        ten = iten.getText();
        sdt = isdt.getText();
        if(rnam.isSelected())
          gt = "Nam";
        else
          gt="Nữ";    
        monday = cbmonday.getSelectedItem().toString();
        new Controller.QLGVController().themGV(ten, gt, sdt, monday);
        dataTable.setModel(new GiangVienCustomTable(Model.Main_Model.DanhSachGV));
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(ima.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Hãy chọn một hàng để sửa");
            return;
        }
        ma = ima.getText();
        ten = iten.getText();
        sdt = isdt.getText();
        if(rnam.isSelected())
          gt = "Nam";
        else
          gt="Nữ";    
        monday = cbmonday.getSelectedItem().toString();
        new Controller.QLGVController().suaGV(ma,ten, gt, sdt, monday); 
        dataTable.setModel(new GiangVienCustomTable(Model.Main_Model.DanhSachGV));
    }//GEN-LAST:event_jButton2ActionPerformed

