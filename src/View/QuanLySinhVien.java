    private void btimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btimActionPerformed
        if(ima.getText().isEmpty() && iten.getText().isEmpty() && !btim.isVisible() ){
            JOptionPane.showMessageDialog(null,"Mời nhập giá trị");
            return;
        }
        
        if(ima.isEnabled() == true){
            controller.timMa(ima.getText().toString());
            if(Main_Model.DanhSachSV.isEmpty()){
                JOptionPane.showMessageDialog(null,"Sinh viên không tồn tại");
                return;
            }
            dataTable.setModel(new SinhVienCustomTable(Main_Model.DanhSachSV));
        }
        if(cbmakhoa.isEnabled() == true){
            controller.timKhoa(cbmakhoa.getSelectedItem().toString());
            dataTable.setModel(new SinhVienCustomTable(Main_Model.DanhSachSV));
        }
        if(iten.isEnabled() == true){
            controller.timTen(iten.getText().toString());
            if(Main_Model.DanhSachSV.isEmpty()){
                JOptionPane.showMessageDialog(null,"Sinh viên không tồn tại");
                return;
            }
            dataTable.setModel(new SinhVienCustomTable(Main_Model.DanhSachSV));
        }
    }//GEN-LAST:event_btimActionPerformed

