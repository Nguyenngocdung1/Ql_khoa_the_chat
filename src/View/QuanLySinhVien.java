    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!isFill()) return;
        ten = iten.getText();
        sdt = isdt.getText();
        if(rnam.isSelected())
          gt = "Nam";
        else
          gt="Nữ";    
        makhoa = cbmakhoa.getSelectedItem().toString();
        controller.themSV(ten, gt, sdt, makhoa); 
        dataTable.setModel(new SinhVienCustomTable(Model.Main_Model.DanhSachSV));
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void dataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTableMouseClicked
        int i = dataTable.getSelectedRow();
        SinhVien selectedRow = Main_Model.DanhSachSV.get(i);
        ima.setText(selectedRow.getMa());
        iten.setText(selectedRow.getTen());
        isdt.setText(selectedRow.getSdt());
        cbmakhoa.getModel().setSelectedItem(selectedRow.getMaKhoa().toString());
        if(selectedRow.getGioiTinh().equals("Nam"))
            rnam.setSelected(true);
        else 
            rnu.setSelected(true);
    }//GEN-LAST:event_dataTableMouseClicked

