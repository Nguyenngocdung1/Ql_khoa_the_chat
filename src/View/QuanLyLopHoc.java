    private void jTableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDataMouseClicked
        // TODO add your handling code here:
        int i = jTableData.getSelectedRow();
        LopHoc selectedRow = Main_Model.DanhSachLH.get(i);
        jMaLh.setText(selectedRow.getMaLop());
        jMaGv.setText(selectedRow.getMaGv());
        jNgayBatDau.setText(selectedRow.getNgayBatDau());
        jTenLopHoc.setText(selectedRow.getTenLop());
        jTietHoc.setText(Integer.toString(selectedRow.getTietHoc()));
        jTongTiet.setText(Integer.toString(selectedRow.getTongTiet()));
        jHocPhi.setText(Double.toString(selectedRow.getHocPhi()));
    }//GEN-LAST:event_jTableDataMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jMaLh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hãy chọn một hàng để sửa");
            return;
        }
        String maLh = jMaLh.getText();
        maGv = jMaGv.getText();
        tenLop = jTenLopHoc.getText();
        ngayBatDau = jNgayBatDau.getText();
        tietHoc = Integer.parseInt(jTietHoc.getText());
        tongTiet = Integer.parseInt(jTongTiet.getText());
        hocPhi = Double.parseDouble(jHocPhi.getText());
        new Controller.QLLHController().suaLH(maLh, maGv, tenLop, ngayBatDau, tongTiet, tietHoc, hocPhi);
        jTableData.setModel(new LopHocCustomTable(Model.Main_Model.DanhSachLH));
    }//GEN-LAST:event_jButton1ActionPerformed

