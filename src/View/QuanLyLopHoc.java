    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jMaLh.getText().isEmpty() && jMaGv.getText().isEmpty() && jTenLopHoc.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Mời nhập giá trị");
            return;
        }
        
        if(jMaLh.isEnabled() == true){
            controller.timMa(jMaLh.getText().toString());
            if(Main_Model.DanhSachLH.isEmpty()){
                JOptionPane.showMessageDialog(null,"Mã lớp học không tồn tại");
                return;
            }
            jTableData.setModel(new LopHocCustomTable(Main_Model.DanhSachLH));
        }
        if(jMaGv.isEnabled() == true){
            controller.timMaGv(jMaGv.getText().toString());
            if(Main_Model.DanhSachLH.isEmpty()){
                JOptionPane.showMessageDialog(null,"Mã giảng viên không tồn tại");
                return;
            }
            jTableData.setModel(new LopHocCustomTable(Main_Model.DanhSachLH));
        }
        if(jTenLopHoc.isEnabled() == true){
            controller.timTen(jTenLopHoc.getText().toString());
           if(Main_Model.DanhSachLH.isEmpty()){
                JOptionPane.showMessageDialog(null,"Tên lớp học không tồn tại");
                return;
            }
            jTableData.setModel(new LopHocCustomTable(Main_Model.DanhSachLH));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

